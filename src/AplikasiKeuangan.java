    private void btnEksporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEksporActionPerformed
        JFileChooser fileChooser = new JFileChooser();
            fileChooser.setDialogTitle("Simpan sebagai CSV");
            fileChooser.setFileFilter(new FileNameExtensionFilter("CSV File", "csv"));

            int userSelection = fileChooser.showSaveDialog(this);

            if (userSelection == JFileChooser.APPROVE_OPTION) {
                File fileToSave = fileChooser.getSelectedFile();
                // Pastikan file memiliki ekstensi .csv
                if (!fileToSave.getAbsolutePath().endsWith(".csv")) {
                    fileToSave = new File(fileToSave.getAbsolutePath() + ".csv");
                }

                try (FileWriter writer = new FileWriter(fileToSave)) {
                    // Tulis Header CSV
                    writer.append("ID,Deskripsi,Jumlah,Tipe,Tanggal(ISO)\n");

                    // Tulis data
                    List<Transaksi> semuaData = dao.getAll(); // Ambil semua data
                    for (Transaksi tx : semuaData) {
                        writer.append(tx.getId()).append(",");
                        // Handle jika deskripsi mengandung koma (CSV standar)
                        writer.append("\"").append(tx.getDeskripsi().replace("\"", "\"\"")).append("\"").append(","); 
                        writer.append(String.valueOf(tx.getJumlah())).append(",");
                        writer.append(tx.getTipe().name()).append(",");
                        writer.append(formatDate.format(tx.getTanggal())); // Format ISO
                        writer.append("\n");
                    }
                    writer.flush();
                    JOptionPane.showMessageDialog(this, "Data berhasil diekspor ke " + fileToSave.getName(), "Sukses", JOptionPane.INFORMATION_MESSAGE);
                } catch (IOException e) {
                    JOptionPane.showMessageDialog(this, "Gagal menyimpan file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            }        // TODO add your handling code here:
    }//GEN-LAST:event_btnEksporActionPerformed

    private void btnImporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImporActionPerformed
        JFileChooser fileChooser = new JFileChooser();
            fileChooser.setDialogTitle("Pilih File CSV untuk Impor");
            fileChooser.setFileFilter(new FileNameExtensionFilter("CSV File", "csv"));

            int userSelection = fileChooser.showOpenDialog(this);

            if (userSelection == JFileChooser.APPROVE_OPTION) {
                File fileToOpen = fileChooser.getSelectedFile();

                int konfirmasi = JOptionPane.showConfirmDialog(this, 
                    "Impor file akan menghapus semua data saat ini. Lanjutkan?", 
                    "Konfirmasi Impor", 
                    JOptionPane.YES_NO_OPTION, 
                    JOptionPane.WARNING_MESSAGE);

                if (konfirmasi != JOptionPane.YES_OPTION) {
                    return;
                }

                List<Transaksi> dataBaru = new ArrayList<>();
                try (BufferedReader reader = new BufferedReader(new FileReader(fileToOpen))) {
                    String line = reader.readLine(); // Baca header, abaikan

                    if (line == null || !line.startsWith("ID,Deskripsi")) {
                         JOptionPane.showMessageDialog(this, "Format file CSV tidak valid.", "Error", JOptionPane.ERROR_MESSAGE);
                         return;
                    }

                    while ((line = reader.readLine()) != null) {
                        // Regex sederhana untuk memecah CSV
                        String[] parts = line.split(",(?=(?:[^\"]*\"[^\"]*\")*[^\"]*$)", -1);

                        if (parts.length >= 5) {
                            String id = parts[0];
                            String deskripsi = parts[1].replace("\"", ""); // Hapus tanda kutip
                            double jumlah = Double.parseDouble(parts[2]);
                            TipeTransaksi tipe = TipeTransaksi.valueOf(parts[3]);
                            Date tanggal = formatDate.parse(parts[4]); // Parse dari format ISO

                            dataBaru.add(new Transaksi(id, deskripsi, jumlah, tipe, tanggal));
                        }
                    }

                    dao.setAll(dataBaru); // Timpa data di DAO
                    refreshUI(); // Segarkan UI
                    bersihkanForm();
                    JOptionPane.showMessageDialog(this, "Data berhasil diimpor.", "Sukses", JOptionPane.INFORMATION_MESSAGE);

                } catch (IOException | ParseException | IllegalArgumentException e) {
                    e.printStackTrace(); // Tampilkan error di console
                    JOptionPane.showMessageDialog(this, "Gagal membaca file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            }        // TODO add your handling code here:
    }//GEN-LAST:event_btnImporActionPerformed

