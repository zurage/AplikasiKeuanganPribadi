    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
            if (transaksiTerpilih == null) {
            JOptionPane.showMessageDialog(this, "Pilih transaksi yang ingin diubah.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // Ambil data baru dari form
            String deskripsi = txtDeskripsi.getText();
            double jumlah = Double.parseDouble(txtJumlah.getText());
            TipeTransaksi tipe = radioPemasukan.isSelected() ? TipeTransaksi.PEMASUKAN : TipeTransaksi.PENGELUARAN;

            if (deskripsi.isEmpty() || jumlah <= 0) {
                JOptionPane.showMessageDialog(this, "Deskripsi tidak boleh kosong dan Jumlah harus > 0.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Update data di objek yang terpilih
            transaksiTerpilih.setDeskripsi(deskripsi);
            transaksiTerpilih.setJumlah(jumlah);
            transaksiTerpilih.setTipe(tipe);

            dao.ubah(transaksiTerpilih); // Kirim objek yang sudah diupdate ke DAO

            refreshUI();
            bersihkanForm();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka yang valid.", "Error", JOptionPane.ERROR_MESSAGE);
        }      // TODO add your handling code here:
    }//GEN-LAST:event_btnUbahActionPerformed

